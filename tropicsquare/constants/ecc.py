"""ECC (Elliptic Curve Cryptography) constants for TropicSquare library.

This module contains all ECC-related constants including curve types,
key origin values, and maximum key slots.
"""

# Maximum number of ECC key slots
ECC_MAX_KEYS = 31

# Curve type constants
ECC_CURVE_P256 = 0x01      # NIST P-256 curve (secp256r1)
ECC_CURVE_ED25519 = 0x02   # Ed25519 curve (EdDSA)

# Key origin constants
ECC_KEY_ORIGIN_GENERATED = 0x01  # Key was generated by the chip
ECC_KEY_ORIGIN_STORED = 0x02     # Key was imported via ecc_key_store()
